package com.vaadin.battle.station.backend;

public class TravelAllowance {
    private float lower_lim;
    private float upper_lim;
    private float ta;

    public float getLower_lim() {
        return lower_lim;
    }

    public void setLower_lim(float lower_lim) {
        this.lower_lim = lower_lim;
    }

    public float getUpper_lim() {
        return upper_lim;
    }

    public void setUpper_lim(float upper_lim) {
        this.upper_lim = upper_lim;
    }

    public float getTa() {
        return ta;
    }

    public void setTa(float ta) {
        this.ta = ta;
    }
}
